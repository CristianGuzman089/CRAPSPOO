    private void botonDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonDadosActionPerformed
        partida.lanzarDado();               
        valorDado.setText(partida.valorLanzada());

        if (resultado() == 1) {
            JOptionPane.showMessageDialog(this, "Empezando nueva Partida");
            valor = 0;
            cambiarJugador();
            participacionT = false;
            botonDados.setEnabled(participacionT);
            apostar.setEnabled(!participacionT);
        }else{
            JOptionPane.showMessageDialog(this, "CRAPS, ¡vuelva a lanzar!");
            labelPrimerValor.setText("Primer tiro: "+Integer.toString(partida.getPrimerTiro()));
            apostar.setEnabled(false);
        }

    }//GEN-LAST:event_botonDadosActionPerformed

    private void apostarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apostarActionPerformed
        if (cajaDeTexto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un valor numerico");
        } else {
            int numJText = Integer.parseInt(cajaDeTexto.getText());
            if (numJText > listaJugadores.get(valor).getSaldo()) {
                JOptionPane.showMessageDialog(this, "Valor fuera de los limites de su saldo!");
                cajaDeTexto.setText("");
            } else {
                if (valor == cantidadJugadores - 1) {
                    valorApostado[valor] = Integer.parseInt(cajaDeTexto.getText());
                    valor++;
                    cajaDeTexto.setText("");
                    participacionT = true;
                    botonDados.setEnabled(participacionT);
                    apostar.setEnabled(!participacionT);
                } else {
                    valorApostado[valor] = Integer.parseInt(cajaDeTexto.getText());
                    valor++;
                    cambiarJugador();
                    cajaDeTexto.setText("");
                }
            }
        }
    }//GEN-LAST:event_apostarActionPerformed

