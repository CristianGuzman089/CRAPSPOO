    public void llenarLista() {
        def.removeAllElements();
        for (int i = 0; i < cantidadJugadores; i++) {
            def.addElement(listaJugadores.get(i).mostrarDatos());
        }
    }

    public void cambiarJugador() {
        nombreJugador.setText(listaJugadores.get(valor).getNombre());
        saldoJugador.setText(listaJugadores.get(valor).getStringSaldo());
        participacionT = false;
        botonDados.setEnabled(participacionT);
    }
    

    public int resultado() {
        int i;
        if (partida.resultado() == partida.getGanar()) {
            for (i = 0; i < cantidadJugadores; i++) {
                JOptionPane.showMessageDialog(this, listaJugadores.get(i).getNombre() + " ganó :" + valorApostado[i] * 2);
                listaJugadores.get(i).setSaldo((valorApostado[i] * 2) + listaJugadores.get(i).getSaldo());
                llenarLista();
                partida.setPrimerTiro(0);
            }
            return 1;
        }
        if (partida.resultado() == partida.getPerder()) {
            for (i = 0; i < cantidadJugadores; i++) {
                JOptionPane.showMessageDialog(this, listaJugadores.get(i).getNombre() + " perdio : " + valorApostado[i]);
                listaJugadores.get(i).setSaldo(listaJugadores.get(i).getSaldo() - (valorApostado[i]));
                if(listaJugadores.get(i).getSaldo()==0){
                    JOptionPane.showMessageDialog(this, listaJugadores.get(i).getNombre() + " ha sido ELIMINADO!");
                    listaJugadores.remove(i);
                    cantidadJugadores = listaJugadores.size();
                    if(listaJugadores.isEmpty()){
                        JOptionPane.showMessageDialog(this, "¡NO QUEDA NINGUN JUGADOR! Juego terminado...");
                        System.exit(0);
                    }
                }
                llenarLista();
                partida.setPrimerTiro(0);
            }
            return 1;
        }
        if (partida.resultado() == partida.getCraps()) {
            return 2;
        }
        return 0;
    }

